export function getPageAnnotation(pathname: string): PageAnnotation | null {
  if (pathname.startsWith('/campaign/edit')) {
    return pageAnnotations['/campaign/new']
  }

  if (pathname.startsWith('/campaign/new')) {
    return pageAnnotations['/campaign/new']
  }

  if (pathname.startsWith('/creator-management/detail')) {
    return pageAnnotations['/creator-management/detail']
  }

  if (pathname.startsWith('/creator-management')) {
    return pageAnnotations['/creator-management']
  }

  if (pathname.startsWith('/creator-level') || pathname.startsWith('/creator-data')) {
    return pageAnnotations['/creator-level']
  }

  if (pathname.startsWith('/campaign')) {
    return pageAnnotations['/campaign']
  }

  if (pathname.startsWith('/work-management') || pathname.startsWith('/work-review')) {
    return pageAnnotations['/work-management']
  }

  return pageAnnotations[pathname] ?? null
}
